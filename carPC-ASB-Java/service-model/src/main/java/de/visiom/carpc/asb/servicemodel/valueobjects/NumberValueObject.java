package de.visiom.carpc.asb.servicemodel.valueobjects;

import java.text.NumberFormat;
import java.text.ParseException;

public class NumberValueObject implements ValueObject {

    private final Number value;

    protected NumberValueObject(Number value) {
        this.value = value;
    }

    public static NumberValueObject valueOf(Number number) {
        return new NumberValueObject(number);
    }

    public static NumberValueObject valueOf(String string) throws ParseException {
        return valueOf(NumberFormat.getInstance().parse(string));
    }

    public Number getValue() {
        return value;
    }

    @Override
    public String toString() {
        return value.toString();
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((value == null) ? 0 : value.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (!(obj instanceof NumberValueObject)) {
            return false;
        }
        NumberValueObject other = (NumberValueObject) obj;
        if (value == null) {
            if (other.value != null) {
                return false;
            }
        } else if (!value.equals(other.value)) {
            return false;
        }
        return true;
    }

}
